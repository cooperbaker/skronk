from ENCODER import encoder
import time


#-------------------------------------------------------------------------------
# pins
#-------------------------------------------------------------------------------
E1A = 24
E1B = 25
E2A = 26
E2B = 27


enc = encoder( E1A, E1B )

def inc( val ):
    print( "encoder increment - %s" % val )

def dec( val ):
    print( "encoder decrement - %s" % val )

enc.inc = inc
enc.dec = dec

while True:
    enc.read()
    time.sleep( 0.001 )


