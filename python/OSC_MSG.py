from pythonosc import udp_client

#-------------------------------------------------------------------------------
# osc_msg class
#-------------------------------------------------------------------------------
class osc_msg():

    # locals
    ip     = '0.0.0.0'
    port   = 0
    client = 0

    # constructor
    def __init__( self, ip, port ):
        self.ip     = ip
        self.port   = port
        self.client = udp_client.SimpleUDPClient( self.ip, self.port )

    # send a message
    def send( self, addr, val ):
        self.client.send_message( addr, val )


